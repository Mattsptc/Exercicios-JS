// function declaration - da pra chamar a function antes de declarar
function soma(x, y) {
    return x + y
}

// function expression - so da para chamar apos ser declarada
const sub = function (x, y) {
    return x - y
}

// named function expression- so da para chamar apos ser declarada

const mult = function mult (x, y) {
    return x * y 
}

